#include "libft.h"

char	*ft_strncpy(char *dest, const char *src, size_t n)
{
	size_t			index;
	const size_t	src_len = ft_strlen(src);

	index = 0;
	while (index < src_len)
	{
		dest[index] = src[index];
		index++;
	}
	while (index < n)
	{
		dest[index] = '\0';
		index++;
	}
	return (dest);
}
